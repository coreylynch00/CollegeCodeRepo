#  Description:  Illustrates to types of division
tips = 29
noOfPeople = 3
amountPerPerson = tips / noOfPeople
print('Tip per person ', amountPerPerson)

cakes = 10
cakesPerPerson = cakes // noOfPeople
print('Cakes per person' ,cakesPerPerson)
cakesLeftOver = cakes % noOfPeople
print('Left over cakes ',cakesLeftOver)
